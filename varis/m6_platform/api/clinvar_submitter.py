def _build_evidence_comment(variant_record: VariantRecord) -> str:
    """Build the evidence summary comment for a ClinVar submission.

    Includes ensemble score with confidence interval, structural
    features, evidence tags, model version, and the mandatory
    disclaimer.

    Args:
        variant_record: A fully-scored VariantRecord.

    Returns:
        A multi-line evidence comment string.
    """
    parts: list[str] = []

    # Ensemble score with CI and agreement
    score = variant_record.score_ensemble
    lower = variant_record.confidence_lower
    upper = variant_record.confidence_upper
    agreement = variant_record.model_agreement or "unknown"
    if score is not None:
        ci_str = ""
        if lower is not None and upper is not None:
            ci_str = f" (95% CI: {lower:.2f}-{upper:.2f})"
        parts.append(
            f"Ensemble pathogenicity score: {score:.2f}{ci_str}, "
            f"model agreement: {agreement}."
        )

    # DDG value
    ddg = variant_record.ddg_mean
    if ddg is not None:
        parts.append(f"Predicted stability change (DDG): {ddg:.1f} kcal/mol.")

    # Burial category
    burial = variant_record.burial_category
    if burial is not None:
        parts.append(f"Burial category: {burial}.")

    # Domain info
    domain = variant_record.domain_name
    if domain is not None:
        criticality = variant_record.domain_criticality or "unknown"
        parts.append(f"Protein domain: {domain} (criticality: {criticality}).")

    # Conservation score
    conservation = variant_record.conservation_score
    if conservation is not None:
        parts.append(f"Conservation score: {conservation:.2f}.")

    # Secondary structure
    ss_name = variant_record.secondary_structure_name
    if ss_name is not None:
        parts.append(f"Secondary structure: {ss_name}.")

    # Evidence tags
    tags = variant_record.evidence_tags
    if tags:
        parts.append(
            f"Suggested computational evidence tags: {', '.join(tags)}."
        )

    # Model version
    ensemble_version = variant_record.ensemble_version or "unknown"
    parts.append(f"Ensemble model version: {ensemble_version}.")

    # Disclaimer
    parts.append(_DISCLAIMER)

    return " ".join(parts)
